#!/usr/bin/env node
// Regenerate the built-in GOLD default favicon (the orange-tab fix baseline).
//
// SINGLE SOURCE OF TRUTH: public/brand/setnayan-mark.svg (champagne-gold mark).
// This packs a real multi-size .ico (16/32/48 PNG-in-ICO) and embeds it as a
// base64 string in app/favicon.ico/default-favicon.ts. The dynamic favicon
// route serves these bytes whenever no admin brand icon is set — so the bare
// /favicon.ico path always returns a genuine gold .ico (content-type
// image/x-icon), which is what kills the stale orange Safari tab.
//
// Embedding (vs. reading a file at runtime) guarantees the default is always
// available in the serverless bundle regardless of public/ tracing, R2, or DB.
//
//   pnpm --filter @setnayan/web brand:favicon   (or: node scripts/gen-default-favicon.mjs)
//
// The packer below is a plain-JS twin of lib/ico.ts (which is 'server-only' TS
// and can't be imported from this .mjs build script). Keep the two in sync.
import sharp from 'sharp';
import { fileURLToPath } from 'node:url';
import { dirname, join, relative } from 'node:path';
import { writeFileSync, readFileSync } from 'node:fs';

const web = join(dirname(fileURLToPath(import.meta.url)), '..');
const repo = join(web, '..', '..');
const SRC = join(web, 'public/brand/setnayan-mark.svg');
const OUT = join(web, 'app/favicon.ico/default-favicon.ts');

const SIZES = [16, 32, 48];

// --- ICO packer (twin of lib/ico.ts) ----------------------------------------
function packIco(images) {
  const HEADER = 6;
  const ENTRY = 16;
  const dirSize = HEADER + ENTRY * images.length;
  const header = Buffer.alloc(HEADER);
  header.writeUInt16LE(0, 0);
  header.writeUInt16LE(1, 2);
  header.writeUInt16LE(images.length, 4);
  const entries = [];
  let offset = dirSize;
  for (const { size, png } of images) {
    const e = Buffer.alloc(ENTRY);
    e.writeUInt8(size >= 256 ? 0 : size, 0);
    e.writeUInt8(size >= 256 ? 0 : size, 1);
    e.writeUInt8(0, 2);
    e.writeUInt8(0, 3);
    e.writeUInt16LE(1, 4);
    e.writeUInt16LE(32, 6);
    e.writeUInt32LE(png.length, 8);
    e.writeUInt32LE(offset, 12);
    entries.push(e);
    offset += png.length;
  }
  return Buffer.concat([header, ...entries, ...images.map((i) => i.png)]);
}

const svg = readFileSync(SRC);
const images = [];
for (const size of SIZES) {
  const png = await sharp(svg, { density: 384 })
    .resize(size, size, {
      fit: 'contain',
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    })
    .png()
    .toBuffer();
  images.push({ size, png });
}

const ico = packIco(images);
const base64 = ico.toString('base64');

const module = `// AUTO-GENERATED by scripts/gen-default-favicon.mjs — DO NOT EDIT BY HAND.
//
// Built-in GOLD default favicon (16/32/48 PNG-in-ICO) derived from the
// canonical public/brand/setnayan-mark.svg. The dynamic favicon route
// (app/favicon.ico/route.ts) serves these bytes when no admin brand icon is
// set. To refresh after changing the brand mark: pnpm --filter @setnayan/web
// brand:favicon.

/** Base64 of the multi-size gold default .ico. */
export const DEFAULT_FAVICON_ICO_BASE64 = '${base64}';

/** Decoded bytes, ready to return from the route handler. */
export const DEFAULT_FAVICON_ICO = Buffer.from(DEFAULT_FAVICON_ICO_BASE64, 'base64');
`;

writeFileSync(OUT, module);
console.log(
  `✓ ico  ${relative(repo, OUT)}  (${SIZES.join('/')} px · ${ico.length} bytes · base64 ${base64.length} chars)`,
);
